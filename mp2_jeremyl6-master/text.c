/* Functional buffer for status bar specifically */
unsigned char status_buffer[5760];

/*
 *  text_to_buffer
 *        DESCRIPTION : Translates text passed in onto the buffer, aligns based on
 *                         current mode and purpose
 *             INPUTS : const char *text - String of Current Text from Game
 *                      int mode - Alignment mode, decides where to draw text and how
 *                          to align it on the status bar
 *            OUTPUTS : None
 *       RETURN VALUE : None
 *       SIDE EFFECTS : Writes the string to the buffer so that it can be displayed
 *                      on the status bar
 */
void text_to_buffer( const char* room_status, const char* typed_command, int alignment_mode )
{
    /*
        Note: we DON'T want to reset the entire status bar, as we want to preserve
        the current room being displayed when we type, in addition to previous
        letters typed. 
    */
    /* Because we aren't in C99, declaring vars inside for loops is NOT allowed. Declare here. */
    int i;
    int start_status = 0;
    int start_room = 0;
    int start_command = 0;
    /* Calculate alignment based on mode*/
    switch( alignment_mode )
    {
        /* Clear status buffer by setting entire buffer to one color and return if set to "clear" */
        case clear:
            for( i = 0; i < 5760; i++ ) 
            {
                status_buffer[ i ] = 0x03;
            }
            return;
        
        /* Print the status message passed in by the game. */
        case status_msg_mode:
            /* Clear status bar before we print anything */
            for( i = 0; i < 5760; i++ ) 
            {
                status_buffer[ i ] = 0x03;
            }    
            
            /* Print the status message; set the texts' position */
            start_status = ( SCREEN_WIDTH - ( strlen( room_status ) * CHAR_WIDTH ) ) / 2;
            write_text_to_bar( room_status, start_status );

            break;
        
        /* Print the room name and the typed command. */
        case room_command:
            /* Clear status bar before we print anything */
            for( i = 0; i < 5760; i++ ) 
            {
                status_buffer[ i ] = 0x03;
            }    
            
            /* Print the typed command; set the texts' starting position. */
            start_command = SCREEN_WIDTH - ( strlen( typed_command ) * CHAR_WIDTH );
            
            if( strlen( typed_command ) < MAX_TYPED_LEN )
            {
                char* underscore = "_";
                write_text_to_bar( typed_command, start_command - FONT_WIDTH );
                write_text_to_bar( underscore, SCREEN_WIDTH - FONT_WIDTH );

            }
            else
            {
                write_text_to_bar( typed_command, start_command );
            }
            
            /* ALso print the room name; set the texts' starting position. */
            write_text_to_bar( room_status, start_room );

            break;

    }

}



void 
write_text_to_bar( const char* string, int start )
{
    int string_index, pixel_row_index, pixel_col_index;

    /* Iterate through string's characters */
    for ( string_index = 0; string_index < strlen( string ); string_index++ )
    {
        /* Iterate through character's rows */        
        for ( pixel_row_index = 0; pixel_row_index < CHAR_HEIGHT; pixel_row_index++ )
        {
            /* Font is accessed by font_data[ ascii value ][ row of pixels ] */
            /* Get font data of current character */
            int cur_char = font_data[ (int)string[ string_index ] ][ pixel_row_index ];
            int current_bit = 0x0080;
            /* Iterate through row's columns, mask bits to print correct column */
            for ( pixel_col_index = 0; pixel_col_index < CHAR_WIDTH; pixel_col_index++ )
            {
                /* To calculate location in buffer we need to calculate which plane we are */
                /* writing to with STATUS_BAR_SIZE * plane_offset. Then, we need to        */
                /* calculate where within the plane we are writing to, dictated by         */
                /* the row offset ( Image Width / # of planes * [ current row + 1 ] )      */
                /* [Note: we do current row + 1 bc our character should have one pixel b/w */
                /* the top of the character and the top of our status bar]                 */
                /* and column offset, defined as our alignment start + character # +       */
                /* current column, divided by the number of planes.                        */

                /* Plane offset - switch which plane we write to with each row */
                int plane_offset = pixel_col_index & 3;
                
                /* Calculate starting location in buffer */
                int buffer_location = STATUS_BAR_SIZE * plane_offset;
                
                /* Calculate row offset in buffer */
                int row_offset = SCREEN_WIDTH * ( pixel_row_index + 1 ) / NUM_PLANES;
                
                /* Calculate column/start/alignment offset in buffer */
                int start_offset = ( start + 8 * string_index + pixel_col_index ) / NUM_PLANES;
                
                /* Combine to find index in buffer that corresponds to our pixel */
                int buffer_index = buffer_location + row_offset + start_offset;

                /* If the corresponding pixel is set to 1, then that means we print that pixel as color */
                if( cur_char & current_bit )
                {
                    status_buffer[ buffer_index ] = 0x00;
                }

                /* Shift bit that we're looking at in row to the right by 1 */
                current_bit >>= 1;
            }

        }
        
    }

}


